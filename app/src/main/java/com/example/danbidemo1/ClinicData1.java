package com.example.danbidemo1;

public class ClinicData1 {
    private String profile;
    private String clinic_expertise;
    private String clinic_address;
    private String clinic_name;
    private long clinic_rating;
    private String clinic_email;
    private String clinic_phoneNumber;

    public ClinicData1() {}

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getClinic_expertise() {
        return clinic_expertise;
    }

    public void setClinic_expertise(String clinic_expertise) {
        this.clinic_expertise = clinic_expertise;
    }

    public String getClinic_address() {
        return clinic_address;
    }

    public void setClinic_address(String clinic_address) {
        this.clinic_address = clinic_address;
    }

    public String getClinic_name() {
        return clinic_name;
    }

    public void setClinic_name(String clinic_name) {
        this.clinic_name = clinic_name;
    }

    public long getClinic_rating() {
        return clinic_rating;
    }

    public void setClinic_rating(long clinic_rating) {
        this.clinic_rating = clinic_rating;
    }

    public String getClinic_email() {
        return clinic_email;
    }

    public void setClinic_email(String clinic_email) {
        this.clinic_email = clinic_email;
    }

    public String getClinic_phoneNumber() {
        return clinic_phoneNumber;
    }

    public void setClinic_phoneNumber(String clinic_phoneNumber) {
        this.clinic_phoneNumber = clinic_phoneNumber;
    }
}
